#Mudar um item da lista
frutas = ["uva", "morango", "laranja"]
frutas [2] = "maçã"
print (frutas)

#Mudar dois item da lista
frutas = ["uva", "morango", "laranja"]
frutas [1:2] = ["maçã", "abacaxi"]
print (frutas)


